export const frenchToEnglish: Record<string, string> = Object.entries(englishToFrench).reduce<Record<string, string>>(
  (translations, [english, french]) => {
    if (english !== french && translations[french] === undefined) {
      translations[french] = english;
    }

    return translations;
  },
  {}
);

const wordPattern = /[\p{L}\p{N}]/u;

function escapeRegExp(value: string) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function createEntries(source: Record<string, string>) {
  return Object.entries(source).sort(([left], [right]) => right.length - left.length);
}

const englishToFrenchEntries = createEntries(englishToFrench);
const frenchToEnglishEntries = createEntries(frenchToEnglish);

function replacePhrase(value: string, from: string, to: string) {
  const startsWithWord = wordPattern.test(from[0] ?? "");
  const endsWithWord = wordPattern.test(from[from.length - 1] ?? "");
  const escaped = escapeRegExp(from);
  const pattern = `${startsWithWord ? "(^|[^\\p{L}\\p{N}])" : ""}(${escaped})${
    endsWithWord ? "(?=$|[^\\p{L}\\p{N}])" : ""
  }`;
  const regex = new RegExp(pattern, "gu");

  return value.replace(regex, (...matches: string[]) => {
    const prefix = startsWithWord ? matches[1] : "";
    return `${prefix}${to}`;
  });
}

export function translateText(value: string, language: Language) {
  const dictionary = language === "fr" ? englishToFrench : frenchToEnglish;
  const entries = language === "fr" ? englishToFrenchEntries : frenchToEnglishEntries;

  if (dictionary[value]) {
    return dictionary[value];
  }

  return entries.reduce((translated, [from, to]) => {
    if (from.length < 3 || !translated.includes(from)) return translated;
    return replacePhrase(translated, from, to);
  }, value);
}
